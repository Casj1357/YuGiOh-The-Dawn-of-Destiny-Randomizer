	{
		fseek(fp,name_to_fight_start_address+(i*8),SEEK_SET);
		fwrite(&newByte,sizeof(newByte),1,fp);
		fseek(fp,picture_address+(i*8),SEEK_SET);
		fwrite(&newByte,sizeof(newByte),1,fp);
	}
	if(DEBUG)
		printf("initialization complete \n");
		
